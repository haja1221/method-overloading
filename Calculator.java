class Calculator{
   
  int add(int a,int b){
    
      return a+b;
  }
  int add(int a,int b,int c){
      
     return a+b+c;
  }
  double multiply(double a,double b){
      
     return a*b;
  } 
  double division(double a,double b){
       
     return a/b;
  }
  int sub(int a,int b){
     
      return a-b;
  }


}

